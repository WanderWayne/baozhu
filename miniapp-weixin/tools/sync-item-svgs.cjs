#!/usr/bin/env node
/**
 * 从 H5 js/data/data-items.js 同步物品图标：
 * 1. assets/icons/*.svg（源）
 * 2. assets/icons/*.png（96×96，小程序运行时 <image> 用）
 * 3. components/item-icon/item-icon.wxs（WXS 查表，零 setData）
 * 用法: node sync-item-svgs.cjs
 */
const fs = require('fs');
const path = require('path');
const sharp = require('sharp');

const ROOT = path.resolve(__dirname, '../..');
const SRC = path.join(ROOT, 'js/data/data-items.js');
const ICON_DIR = path.join(__dirname, '../assets/icons');
const WXS_OUT = path.join(__dirname, '../components/item-icon/item-icon.wxs');
const PNG_SIZE = 96;

function extractItemSvgs(source) {
  const start = source.indexOf('const ITEM_SVGS = {');
  if (start < 0) throw new Error('ITEM_SVGS block not found in H5 data-items.js');
  let depth = 0;
  let end = -1;
  for (let i = start; i < source.length; i += 1) {
    const ch = source[i];
    if (ch === '{') depth += 1;
    else if (ch === '}') {
      depth -= 1;
      if (depth === 0) {
        end = i + 1;
        break;
      }
    }
  }
  if (end < 0) throw new Error('Could not parse ITEM_SVGS block');
  const block = source.slice(start, end);
  // eslint-disable-next-line no-new-func
  return Function(`"use strict"; ${block}; return ITEM_SVGS;`)();
}

function safeFileName(name) {
  return name.replace(/[/\\?%*:|"<>]/g, '_');
}

function buildWxsModule(names, pathByName, emojiByName) {
  const pathLines = names.map((name) => (
    `  ${JSON.stringify(name)}: ${JSON.stringify(pathByName[name])},`
  ));
  const emojiKeys = Object.keys(emojiByName).sort();
  const emojiLines = emojiKeys.map((name) => (
    `  ${JSON.stringify(name)}: ${JSON.stringify(emojiByName[name])},`
  ));

  return [
    '// Auto-generated by tools/sync-item-svgs.cjs — do not edit manually',
    'var PATH = {',
    ...pathLines,
    '};',
    '',
    'var EMOJI = {',
    ...emojiLines,
    '};',
    '',
    'function hasPng(name) {',
    '  return !!name && !!PATH[name];',
    '}',
    '',
    'function src(name) {',
    '  return PATH[name] || "";',
    '}',
    '',
    'function emoji(name, fallback) {',
    '  if (fallback) return fallback;',
    '  if (name && EMOJI[name]) return EMOJI[name];',
    '  return "\\uD83C\\uDF68";',
    '}',
    '',
    'module.exports = {',
    '  hasPng: hasPng,',
    '  src: src,',
    '  emoji: emoji,',
    '};',
    '',
  ].join('\n');
}

async function exportPng(svgContent, outPath) {
  await sharp(Buffer.from(svgContent))
    .resize(PNG_SIZE, PNG_SIZE, { fit: 'contain', background: { r: 0, g: 0, b: 0, alpha: 0 } })
    .png()
    .toFile(outPath);
}

async function main() {
  const source = fs.readFileSync(SRC, 'utf8');
  const ITEM_SVGS = extractItemSvgs(source);
  const names = Object.keys(ITEM_SVGS);

  const { ITEMS } = require(path.join(__dirname, '../data/items.js'));
  const emojiByName = { _diamond: '💎' };
  names.forEach((name) => {
    emojiByName[name] = (ITEMS[name] && ITEMS[name].icon) || '🍨';
  });

  fs.mkdirSync(ICON_DIR, { recursive: true });
  const existing = new Set(fs.readdirSync(ICON_DIR));
  const writtenSvg = new Set();
  const writtenPng = new Set();
  const pathByName = {};

  for (const name of names) {
    const base = safeFileName(name);
    const svgFile = `${base}.svg`;
    const pngFile = `${base}.png`;
    writtenSvg.add(svgFile);
    writtenPng.add(pngFile);

    const svg = ITEM_SVGS[name];
    fs.writeFileSync(path.join(ICON_DIR, svgFile), svg, 'utf8');
    await exportPng(svg, path.join(ICON_DIR, pngFile));

    pathByName[name] = `/assets/icons/${pngFile}`;
  }

  existing.forEach((file) => {
    if ((file.endsWith('.svg') && !writtenSvg.has(file))
      || (file.endsWith('.png') && !writtenPng.has(file))) {
      fs.unlinkSync(path.join(ICON_DIR, file));
    }
  });

  const wxsContent = buildWxsModule(names, pathByName, emojiByName);
  fs.mkdirSync(path.dirname(WXS_OUT), { recursive: true });
  fs.writeFileSync(WXS_OUT, wxsContent, 'utf8');

  console.log(`Exported ${names.length} SVGs + PNGs (${PNG_SIZE}px) to ${ICON_DIR}`);
  console.log(`Wrote ${WXS_OUT}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
